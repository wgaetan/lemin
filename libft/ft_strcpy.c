#include "./includes/libft.h"

char	*ft_strcpy(char *dest, const char *str)
{
	size_t i;

	i = 0;
	while (str[i] != '\0')
	{
		dest[i] = str[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
