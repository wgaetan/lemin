#include "./includes/libft.h"

int		ft_strnequ(const char *s1, const char *s2, size_t n)
{
	if (s1 == NULL || s2 == NULL)
		return (0);
	while (*s1 == *s2 && *s2 && n != 0)
	{
		s1++;
		s2++;
		n--;
	}
	if (n == 0)
		return (1);
	if (*s1 != *s2)
		return (0);
	return (1);
}
